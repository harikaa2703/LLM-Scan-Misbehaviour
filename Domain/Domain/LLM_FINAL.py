    def safe_float(self, x):
        try:
            return float(x)
        except Exception:
            return 0.0

    def clip01(self, x):
        return float(np.clip(x, 0.0, 1.0))

    def scores_from_pipeline(self, raw):
        """
        Converts confirmed flat pipeline output:
            [{"label": "hate", "score": 0.99}, {"label": "nothate", "score": 0.01}]
        into a plain dict:
            {"hate": 0.99, "nothate": 0.01}
        All three models confirmed to return this exact shape from diagnostics.
        """
        if isinstance(raw, list) and len(raw) > 0 and isinstance(raw[0], dict):
            return {r["label"]: self.safe_float(r["score"]) for r in raw}
        if isinstance(raw, dict):
            return {raw["label"]: self.safe_float(raw["score"])}
        return {}

